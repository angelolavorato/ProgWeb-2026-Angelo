function sum(a, b) {
  let resultado = a + b

  return resultado //A palavra-chave return é usada para retornar um valor de uma função. Quando a função é chamada, ela executa o código dentro dela e, quando encontra a palavra-chave return, ela retorna o valor especificado e encerra a execução da função. O valor retorndo pode ser armazenado em uma variável ou usado diretamente em outras partes do código.
}

let resposta = sum(2, 3)
console.log(resposta) //5

console.log(sum(5, 7)) //12
