//Declarando as primeiras funções
function mensagem() {
  console.log('ALO')
} //dentro do parenteses, podemos colocar parâmetros, que são variáveis que recebem valores quando a função é chamada. Já dentro das chaves, colocamos o código que queremos que seja executado quano a função for chamada. Também conhecido como escopo da função.

//Chamando a função
mensagem()//para chamar a função, basta escrever o nome da função seguido de parênteses. Se a função não tiver parâmetro, basta deixar os parênteses vazios. O código dentro da função será executado quando a função for chamada.

