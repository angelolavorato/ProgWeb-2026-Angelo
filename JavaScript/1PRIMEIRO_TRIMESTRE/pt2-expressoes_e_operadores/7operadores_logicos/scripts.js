let email = true //1
let senha = false //0
let isAdmin = true //1

// Operador lógico AND (&&)
console.log(email && senha) //false (1 && 0 = 0)

// Operador lógico OR (||)
console.log(email || senha) //true (1 || 0 = 1  )

// Operador lógico NOT (!)
console.log(!email) //false (!1 = 0)
console.log(!senha) //true (!0 = 1)
console.log(!isAdmin) //false (!1 = 0)
