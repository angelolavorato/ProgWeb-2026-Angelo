let saldo = 500 //saldo em conta
let pagamento = 120 //valor do pagamento

//quero saber se o pagamento é maior ou menor que o saldo da conta
// > operador de comparação "maior que"
console.log(saldo > pagamento) //true, o saldo da conta é maior que o valor do pagamento

// < operador de comparação "menor que"
console.log(saldo < pagamento) //false, o saldo da conta não é menor que o valor do pagamento

// >= operador de comparação "maior ou igual a"
saldo = 120
console.log(saldo >= pagamento) //true, o saldo da conta é igual ao valor do pagamento

// <= operador de comparação "menor ou igual a"
saldo = 100
console.log(saldo <= pagamento) //true, o saldo da conta é menor que o valor do pagamento
